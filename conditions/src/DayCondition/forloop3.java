package DayCondition;

public class forloop3 {

	public static void main(String[] args) {
		// for (initialization;condition;incr/decr)
		for(int i=0; i<=100;i++) {//condition
			if (i%2==0) { //formula foe even
				System.out.println("if no is even:"+i);
				
			}else {
				System.out.println("if no is odd:"+i);
			}
		
		}
	}

}
