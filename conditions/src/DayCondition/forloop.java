package DayCondition;

public class forloop {

	public static void main(String[] args) {
		
		for(int i=1;i<11;i++) { //for print 1 to 10 no.
			System.out.print(i);
		}
		System.out.println("***********reverse******");
		
		for(int i=10;i>0;i--) { //for the reverse 10 to 1 no
			
			System.out.print(i);
		}
		
	}

}
